#include <stdio.h>

int main() {
    int a;  // Variable declaration for integer variable 'a'

    a = 6;  // Assigning the value 6 to variable 'a'
    printf("Value stored in a is %d\n", a);  // Printing the value of 'a' using printf

    a = 1588;  // Changing the value of 'a' to 1588
    printf("Value stored in a is %d\n", a);

    return 0;
}

