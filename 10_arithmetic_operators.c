#include <stdio.h>

int main() {
    int a = 5;
    int b = 3;
    int c = b + a;
    printf("a = %d\n", a);
    printf("b = %d\n", b);
    printf("a + b = %d\n", c);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b); // Returns remainder
    return 0;
}

